const month = 3
switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("Enter correct value between 1 to 12");
        break;
}